package edu.skku.map.finalproject2;

public class LikemovieDataModel {
    private String movieId;
    private String movieTitle;
    private String movieGenre;
    private String movieRelease_date;
    private String movieRuntime;
    private String movieHomepage;


    public String getMovieId() {
        return movieId;
    }

    public void setMovieId(String movieId) {
        this.movieId = movieId;
    }

    public String getMovieTitle() {
        return movieTitle;
    }

    public void setMovieTitle(String movieTitle) {
        this.movieTitle = movieTitle;
    }

    public String getMovieGenre() {
        return movieGenre;
    }

    public void setMovieGenre(String movieGenre) {
        this.movieGenre = movieGenre;
    }

    public String getMovieRelease_date() {
        return movieRelease_date;
    }

    public void setMovieRelease_date(String movieRelease_date) {
        this.movieRelease_date = movieRelease_date;
    }

    public String getMovieRuntime() {
        return movieRuntime;
    }

    public void setMovieRuntime(String movieRuntime) {
        this.movieRuntime = movieRuntime;
    }

    public String getMovieHomepage() {
        return movieHomepage;
    }

    public void setMovieHomepage(String movieHomepage) {
        this.movieHomepage = movieHomepage;
    }
}
